$(function(){
	// 16進RGBコード入力の場合
	$('button').on('click', function() {
		// 変更のあったカラーインデックスと入力値（色）
    let color_idx = $(this).prev('input').attr('id');
    let color_value = $(this).prev('input').val();
    color[color_idx] = color_value;
		// 対象対象クラスのみ色を変える
		change(color_idx);
	});

	// 誕生日入力の場合
	$('input[type=date]').on('change', function() {
		// 変更のあったカラーインデックスと入力値（日付）
    let color_idx = $(this).attr('name');
    let bday = $(this).val().replaceAll('-', '').substr(-4);
		let bdata = birthday_data[bday];
    color[color_idx] = bdata.rgb;
		// 対象対象クラスのみ色を変える
		changeCheckColor(color_idx);
		let td = $(this).closest('td');
		td.next('td').css({'background-color': bdata.rgb});
		td.nextAll('td').eq(1).html(bdata.mean);
	});
});

// 対象クラスのみ色を変える
function changeCheckColor(idx) {
	let classes = getTargetClasses(idx);
	$.each(classes, function(i, v) {
		changeColor(v);
	});
}

// a〜dのいずれかを含む２文字のコンビネーションを取得
function getTargetClasses(target) {
  let rtn = [];
  $.each(all_color_idxs, function(i1, v1) {
    $.each(all_color_idxs, function(i2, v2) {
      if (v1 === target || v2 === target) {
        rtn.push(v1+v2);
      }
    });
  });
  return $.unique(rtn);
}

